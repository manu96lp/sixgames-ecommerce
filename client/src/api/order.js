import { baseApi } from './index';

/* =================================================================================
* 		[ GET /orders ]
* ================================================================================= */

export function getAllOrders( query )
{
    return baseApi.get( `/orders${ query }` );
}

/* =================================================================================
* 		[ POST /orders/:id ]
* ================================================================================= */

export function getOrderById( id )
{
    return baseApi.get( `/orders/${ id }` );
}

/* =================================================================================
* 		[ PUT /orders/:id ]
* ================================================================================= */

export function modifyOrderById( id, data )
{
    return baseApi.put( `/orders/${ id }`, data );
}